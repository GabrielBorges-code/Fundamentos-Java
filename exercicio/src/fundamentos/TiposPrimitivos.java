package fundamentos;

public class TiposPrimitivos {
	public static void main (String [] args) {
		/* Existem 8 tipos primitivos em Java, 
		 * sendo 6 do tipo númerico.
		 * 1 byte representa 8 bits.
		 */

		//byte armazena 1 byte
		byte a;
		//short armazena 2 bytes
		short b;
		//int armazena 3 bytes
		int c;
		//long armazena 4 bytes
		long d;
		//float armazena 8 bytes		
		float e;
		//double armazena 8 bytes
		double f;
		//boolean só aceita true e false
		boolean g;
		//salva caractres: 1, ? a, b,...
		char h;		
		
	}
}
